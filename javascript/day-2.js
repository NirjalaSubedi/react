//logical operator
let age=20;
let value1=age>18 && age<60;
console.log(`can vote:${value1}`);

let value2=age<18||age>60;
console.log(`can not vote:${value2}`);

let value3=!value2;
console.log(`can vote:${value3}`);
console.log("====================");